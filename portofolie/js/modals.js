// VARIABLES
let ribeModal = document.querySelector('#ribeModal');
let excaliburModal = document.querySelector('#excaliburModal');
let alphaModal = document.querySelector('#alphaModal');

// OPEN MODALS
function ribeModalOpen() {
    ribeModal.style.display = 'flex';
}
function excaliburModalOpen() {
    excaliburModal.style.display = 'block';
}
function alphaModalOpen() {
    alphaModal.style.display = 'block';
}

// CLOSE MODALS
function ribeModalClose() {
    ribeModal.style.display = 'none';
}
function excaliburModalClose() {
    excaliburModal.style.display = 'none';
}
function ribeModalClose() {
    alphaModal.style.display = 'none';
}
